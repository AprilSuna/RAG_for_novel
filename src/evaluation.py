def _build_evaluation_prompt(original_text: str, rewritten_text: str, reference_excerpts: list[dict]) -> str:
    """构建评测提示词（v2: 5档评分 + 两层维度 + N/A机制）"""

    # 格式化参考段落
    ref_block = ""
    if reference_excerpts:
        ref_parts = []
        for i, r in enumerate(reference_excerpts, 1):
            technique_note = r.get("technique_note", "无")
            ref_parts.append(
                f"【参考段落 {i}】\n"
                f"标签：{'、'.join(r['tags'])}\n"
                f"精华句：{r.get('highlight', '无')}\n"
                f"技法注释：{technique_note}\n"
                f"原文：\n{r['text']}"
            )
        ref_block = "\n\n".join(ref_parts)
    else:
        ref_block = "（无参考段落）"

    # 格式化评测维度
    dim_block = ""
    for i, dim in enumerate(EVALUATION_DIMENSIONS, 1):
        tier_label = "【通用维度·必评】" if dim["tier"] == "universal" else "【场景维度·可N/A】"
        sub_criteria_str = "；".join(dim["sub_criteria"])
        scoring_lines = "\n".join(f"  {score}分：{desc}" for score, desc in dim["scoring"].items())
        dim_block += (
            f"\n{i}. {tier_label} **{dim['name']}**\n"
            f"  评测标准：{dim['description']}\n"
            f"  青春文学核心：{dim['youth_core']}\n"
            f"  子标准：{sub_criteria_str}\n"
            f"  评分参考：\n{scoring_lines}\n"
            f"  八月长安锚点：{dim['anchors']}\n"
            f"  AI典型错误：{dim['ai_errors']}\n"
        )

    # 构建JSON模板中的维度列表
    dim_json_lines = []
    for dim in EVALUATION_DIMENSIONS:
        na_hint = ""
        if dim["tier"] == "scene":
            na_hint = "（如本段不涉及此维度，applicable填false，score填0，reason说明原因）"
        dim_json_lines.append(
            f'    {{"dimension": "{dim["name"]}", "applicable": true, "score": 0, "reason": "简短说明{na_hint}"}},'
        )
    dim_json_block = "\n".join(dim_json_lines)

    return f"""你是一位文学写作评审专家，精通八月长安的写作风格和青春文学。
请对以下改写文本进行多维度评测打分。

═══════════════════════════════════════
📖 参考标准（八月长安原文）
═══════════════════════════════════════

{ref_block}

═══════════════════════════════════════
📝 待评测文本
═══════════════════════════════════════

【原始输入】
{original_text}

【改写结果】
{rewritten_text}

═══════════════════════════════════════
📊 评测维度与评分标准（9个维度，每个维度1-5分）
═══════════════════════════════════════

维度分两层：
- 【通用维度·必评】：每段都必须打分，不可标记N/A。
- 【场景维度·可N/A】：先判断该维度是否适用于本段文本。如不适用（如无对话、无环境描写、不涉及幽默、纯对话无叙述层），则applicable填false，score填0。

{dim_block}

═══════════════════════════════════════
🎯 评测要求
═══════════════════════════════════════

第一步：判断每个场景维度的适用性。
第二步：对适用维度打1-5分（3分是及格线，5分是八月长安原文水平）。
第三步：输出JSON。

请严格按照以下JSON格式输出评测结果，不要输出任何其他内容：

{{
  "scores": [
{dim_json_block}
  ],
  "overall_score": 0,
  "overall_comment": "一句话总评",
  "improvement_suggestions": ["改进建议1", "改进建议2"]
}}

注意：
- applicable为false的维度，score必须为0，不计入综合评分。
- overall_score 为所有适用维度（applicable为true）得分的平均值。
- 评分要严格，3分是及格线，5分是八月长安原文水平。
- improvement_suggestions 给出2条最关键的改进方向。
- 只输出JSON，不要输出其他任何文字。"""


def evaluate_text(
    original_text: str,
    rewritten_text: str,
    api_key: str = None,
    model: str = DEFAULT_MODEL,
    top_k: int = 2,
) -> dict:
    """
    评测改写文本的质量

    Parameters
    ----------
    original_text : str
        原始输入文本
    rewritten_text : str
        改写后的文本
    api_key : str
        智谱 API Key
    model : str
        评测使用的模型（默认glm-4）
    top_k : int
        检索参考段落数量

    Returns
    -------
    dict
        评测结果，包含各维度评分（含applicable标记）、总评和改进建议
    """
    key = api_key or os.environ.get("ZHIPU_API_KEY", "")
    if not key:
        raise ValueError("缺少智谱 API Key，请设置 ZHIPU_API_KEY 环境变量或传入 api_key 参数")

    # 检索参考段落
    results = search(query=original_text, top_k=top_k)

    # 构建评测prompt
    prompt = _build_evaluation_prompt(original_text, rewritten_text, results)

    # 调用模型
    client = OpenAI(api_key=key, base_url=ZHIPU_API_BASE)
    response = client.chat.completions.create(
        model=model,
        messages=[
            {"role": "system", "content": "你是一位严格的文学写作评审专家，精通八月长安风格和青春文学。评分标准很高，3分是及格线。先判断场景维度的适用性，再对适用维度严格打分。"},
            {"role": "user", "content": prompt},
        ],
        temperature=0.3,
        max_tokens=3000,
    )

    result_text = response.choices[0].message.content.strip()

    # 尝试解析JSON
    try:
        # 去除可能的markdown代码块标记
        if result_text.startswith("```"):
            result_text = result_text.split("\n", 1)[1] if "\n" in result_text else result_text[3:]
        if result_text.endswith("```"):
            result_text = result_text[:-3]
        result_text = result_text.strip()

        result = json.loads(result_text)
        return result
    except json.JSONDecodeError:
        return {
            "error": "评测结果解析失败",
            "raw_response": result_text
        }


def format_evaluation_result(result: dict) -> str:
    """将评测结果格式化为可读文本（v2: 支持N/A机制和5档评分）"""
    if "error" in result:
        return f"⚠️ {result['error']}\n\n原始返回：\n{result.get('raw_response', '')}"

    lines = ["📊 **写作能力评测报告**\n"]
    lines.append("─" * 40 + "\n")

    # 各维度评分
    valid_scores = []
    na_count = 0
    for item in result.get("scores", []):
        dim = item["dimension"]
        score = item.get("score", 0)
        reason = item.get("reason", "")
        applicable = item.get("applicable", True)

        # 处理N/A维度
        if not applicable or (score == 0 and ("不适用" in reason or "无对话" in reason or "不适合" in reason or "不涉及" in reason)):
            na_count += 1
            lines.append(f"⚪ **{dim}**: N/A — {reason}")
        else:
            valid_scores.append(score)
            # 评分条
            bar = "█" * score + "░" * (5 - score)
            icon = "⭐" if score >= 4 else "🔹" if score >= 3 else "🔸"
            lines.append(f"{icon} **{dim}**: {score}/5 `{bar}`")
            lines.append(f"   _{reason}_")
        lines.append("")

    # 综合评分
    overall = result.get("overall_score", 0)
    if not valid_scores:
        calculated = 0
    else:
        calculated = sum(valid_scores) / len(valid_scores)

    lines.append("─" * 40)
    eval_count = len(valid_scores)
    total_count = eval_count + na_count
    lines.append(f"\n🎯 **综合评分: {overall:.1f}/5.0**（基于{eval_count}个适用维度，{na_count}个N/A）\n")
    lines.append(f"📊 计算值: {calculated:.1f}/5.0\n")
    lines.append(f"💬 _{result.get('overall_comment', '')}_\n")

    # 改进建议
    suggestions = result.get("improvement_suggestions", [])
    if suggestions:
        lines.append("📋 **改进建议:**")
        for i, s in enumerate(suggestions, 1):
            lines.append(f"  {i}. {s}")

    return "\n".join(lines)


